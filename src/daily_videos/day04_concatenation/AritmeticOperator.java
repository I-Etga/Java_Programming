package daily_videos.day04_concatenation;

public class AritmeticOperator {
    public static void main(String[] args) {


        System.out.println("100" + 200); // concatenation

        //System.out.println("100" - 55);
            //ARITHMETIC OPERATORS CAN BE USED ONLY WITH NUMBERS.
                // EXCEPTION: ADDITION -NUMBERS AND STRINGS AND ANY DATA

        System.out.println(40-10);
        System.out.println(50/6);
        System.out.println(50 / 6.0);
        System.out.println(50.0/6);
        System.out.println(50d / 6);
        System.out.println(2.5 / 0.5);
       // System.out.println(9 / 0); DIVIDER NEVER CAN BE ZERO

        System.out.println(10 * 2);
        System.out.println(10 / 3);
        System.out.println(10 % 3);





    }
}
