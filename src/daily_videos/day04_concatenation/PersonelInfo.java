package daily_videos.day04_concatenation;

public class PersonelInfo {
    public static void main(String[] args) {

        String firstName = "Ibrahim",
                lastName = "Akay",
                favoriteMusic= "In my Defence-Queen",
                favoriteBook= "War and Peace",
                favorite_Tvshow= "How I met your mother",
                favorite_color = "Orange",
                from = "Turkey";

         // If we use ,(comma) that means we still have more String.
            // If we use ; semicolon that means we've done with these data types.

        char gender = 'M';
        int age = 32;

        boolean isMarried = true,
                isEmployed = false;

        System.out.println("firstName = " + firstName);
        System.out.println("lastName = " + lastName);
        System.out.println("gender = " + gender);
        System.out.println("age = " + age);
        System.out.println("from = " + from);
        System.out.println("isMarried = " + isMarried);
        System.out.println("favoriteMusic = " + favoriteMusic);
        System.out.println("favoriteBook = " + favoriteBook);
        System.out.println("favorite_Tvshow = " + favorite_Tvshow);
        System.out.println("favorite_color = " + favorite_color);
        System.out.println("isMarried = " + isMarried);
        System.out.println("isEmployed = " + isEmployed);
    }
}
